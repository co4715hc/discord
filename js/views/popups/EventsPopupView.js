import BasePopupView from "./BasePopupView.js";

export default class EventsPopupView extends BasePopupView {
    constructor(onClose) {
        super();
        this.onClose = onClose;
    }

    render() {
        this.popupElement = createEventsPopupElement();
        const eventsBox = this.popupElement.querySelector('.popup-events');
        this.mainElement.append(this.popupElement);
        this.closeButton = this.popupElement.querySelector('.popup-events-close');
        this.addEventListener(document, 'click', (event) => {
            if (this.closeButton.contains(event.target) || !eventsBox.contains(event.target) &&
            this.popupElement.contains(event.target)) {
                this.popupElement.classList.add('popup-events-closing');
                setTimeout(() => this.onClose(), 300);
            }
        });
    }
}

